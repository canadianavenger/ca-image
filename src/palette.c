// Greyscale Palettes

// greyscale dac values
img_pal_entry_t gs_dac(uint8_t index, uint16_t shades) {
    uint8_t val = 0;
    
    if((shades > 256) || (shades < 2)) shades = 0;
        uint16_t comp;
    switch(shades){
        case 0:
            break;
        case 2: // Monochome B/W
            index &= 0x01;
            if(index) val = 255;
            break;
        case 4: // 4 Shades
            index &= 0x03;
            val = 85 * index;
            break;
        case 16: // 16 Shades
            index &= 0x0f;
            val = 17 * index;
            break;
        case 256: // 256 shades
            val = index;
            break;
        default: // anything else is considered uncommon, so we calculate the long way
            comp = index;
            comp *= 255;
            comp += shades / 2;
            comp /= (shades-1);
            val = comp;
    }

    img_pal_entry_t pal;
    pal.r = val;
    pal.g = val;
    pal.b = val;

    return pal;
}

void gs_pal(img_pal_entry_t *pal, uint16_t shades) {
    if((shades > 256) || (shades < 2)) return; // we only support up to 8 bit indexing
    for(int index = 0; index < shades; index++) {
        pal[index] = gs_dac(index, shades);
    }
}
