// more accurate palette to what the 5153 display actually showed
// https://int10h.org/blog/2022/06/ibm-5153-color-true-cga-palette/
static const img_pal_entry_t CGA16_ENH[17] = { 
  {0x00,0x00,0x00}, {0x00,0x00,0xc4}, {0x00,0xc4,0x00}, {0x00,0xc4,0xc4},
  {0xc4,0x00,0x00}, {0xc4,0x00,0xc4}, {0xc4,0x7e,0x00}, {0xc4,0xc4,0xc4},
  {0x4e,0x4e,0x4e}, {0x4e,0x4e,0xdc}, {0x4e,0xdc,0x4e}, {0x4e,0xF3,0xF3},
  {0xdc,0x4e,0x4e}, {0xF3,0x4e,0xF3}, {0xF3,0xF3,0x4e}, {0xff,0xff,0xff},
  {0xc4,0xc4,0x00} // no-fix version of color 6
};

// CGA Colour Encoding
//      Bit    7    6    5    4    3    2    1    0
// Function    X    X    X    X    I    R    G    B
// Note: the circuitry has a special case for colour 6 (dark Yellow/Brown)
//       that reduces the itensity of the green component by 50%
//       the calculated table, and DAC function below account for that

// index to colour conversion function
// intens = (index >> 3) & 1
// red   = 85 * (intens | ((index >> 1) & 2))
// green = 85 * (intens | (index & 2))
// blue  = 85 * (intens | ((index << 1) & 2))
// if index == 6 { green >>= 1 } // the "brown fix"
img_pal_entry_t cga_dac(uint8_t index, bool enhanced, bool nofix) {
    index &= 0x0f;
    if(enhanced) {
        if(nofix && (6 == index)) index = 16;
        return CGA16_ENH[index];
    }

    img_pal_entry_t pal;
    uint8_t intens = (index >> 3) & 1;
    pal.r = DAC_LUT[intens | ((index >> 1) & 2)];
    pal.g = DAC_LUT[intens | (index & 2)];
    pal.b = DAC_LUT[intens | ((index << 1) & 2)];
    if((false == nofix) && (6 == index)) pal.g >>= 1; // "brown fix"
    return pal;
}

// Graphics mode palettes as indicies into the full CGA palette
// background is assumed to be black, though in reality it can be programmed to
// any of the 16 colours
const uint8_t CGA_MAP[6][4] = {
    {0,  2,  4,  6},  // mode 4 palette 0 low intensity  [black, dark green, dark red, brown]
    {0, 10, 12, 14},  // mode 4 palette 0 high intensity [black, light green, light red, yellow]
    {0,  3,  5,  7},  // mode 4 palette 1 low intensity  [black, dark cyan, dark magenta, light grey]
    {0, 11, 13, 15},  // mode 4 palette 1 high intensity [black, light cyan, light magenta, white]
    {0,  3,  4,  7},  // mode 5 low intensity            [black, dark cyan, dark red, light gray]
    {0, 11, 12, 15}   // mode 5 high intensity           [black, light cyan, light red, white]
};

void cga_pal(img_pal_entry_t *pal, uint8_t mode, bool enhanced, bool nofix) {
    if(mode < 6) { // handle 4 colour modes 0-5
        for(int index = 0; index < 4; index++) {
            pal[index] = cga_dac(CGA_MAP[mode][index], enhanced, nofix);
        }
        return;
    }
    if(mode != 16) return; // invalid value, return nothing
    // we got here, so we need to return the full 16 colour palette
    if(enhanced) {
        memcpy(pal, CGA16_ENH, sizeof(img_pal_entry_t) * 16);
        if(nofix) pal[6] = CGA16_ENH[16];
        return;
    }

    for(int index = 0; index < 16; index++) {
        pal[index] = cga_dac(index, enhanced, nofix);
    }
}

