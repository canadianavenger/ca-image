// returns the default palette colour for the selected index
img_pal_entry_t vga_dac(uint8_t index) {
    return VGA256[index];
}

// returns the default 16 or 256 colour VGA palette (allows for subsets, always starting at 0)
void vga_pal(img_pal_entry_t *pal, uint16_t colours) {
    if(colours > 256) return;
    memcpy(pal, VGA256, sizeof(img_pal_entry_t) * colours);
}

