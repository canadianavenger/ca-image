// EGA Colour Encoding
//      Bit    7    6    5    4    3    2    1    0
// Function    x    x   R0   G0   B0   R1   G1   B1

// index to colour conversion function
// red   = 85 * (((index >> 1) & 2) | (index >> 5) & 1)
// green = 85 * (( index       & 2) | (index >> 4) & 1)
// blue  = 85 * (((index << 1) & 2) | (index >> 3) & 1)
img_pal_entry_t ega_dac(uint8_t index) {
    index &= 0x3f;

    img_pal_entry_t pal;
    pal.r = DAC_LUT[((index >> 1) & 2) | (index >> 5) & 1];
    pal.g = DAC_LUT[( index       & 2) | (index >> 4) & 1];
    pal.b = DAC_LUT[((index << 1) & 2) | (index >> 3) & 1];
    return pal;
}

// Default EGA system palette as indicies into the full EGA palette
const uint8_t EGA_MAP[16] = {
     0,  1,  2,  3,
     4,  5, 20,  7,
    56, 57, 58, 59,
    60, 61, 62, 63
};

// returns the default 16 colour EGA palette
void ega_pal(img_pal_entry_t *pal) {
    for(int index = 0; index < 16; index++) {
        pal[index] = ega_dac(EGA_MAP[index]);
    }
}

